import React, { useEffect, useState, useCallback } from 'react';
import {
  GameContainer,
  Header,
  Title,
  HeaderQuitButton,
  GameArea,
  InfoContainer,
  BoardArea,
  PlayerCard,
  PlayerAvatar,
  PlayerDetails,
  PlayerHeader,
  PlayerName,
  WallInfo,
  WallIconContainer,
  WallIcon,
  WallCount,
  BoardWrapper,
  Dialog,
  DialogTitle,
  DialogMessage,
  DialogButtons,
  DialogButton,
  Notification,
  GameOverlay,
  PlayerTimer
} from './Game.styles';
import { useNavigate, useLocation, useParams } from 'react-router-dom';
import Board from '../Board';
import { GameState, Position, PlayerInfo, GameStartData, Wall, Player } from '../../types';
import { useSocket } from '../../contexts/SocketContext';


function Game() {
  const { socket } = useSocket();
  const navigate = useNavigate();
  const { roomId } = useParams<{ roomId: string }>();
  const location = useLocation();

  const initialState = location.state as GameStartData | null;

  const [gameState, setGameState] = useState<GameState | null>(initialState?.gameState ?? null);
  const [playerId, setPlayerId] = useState<string | null>(initialState?.playerId ?? null);
  const [playerInfo, setPlayerInfo] = useState<{ me: PlayerInfo; opponent: PlayerInfo } | null>(initialState?.playerInfo ?? null);

  // gameState, playerInfo Í∞í ÏΩòÏÜî Ï∂úÎ†• (ÎîîÎ≤ÑÍπÖÏö©)
  useEffect(() => {
    console.log('gameState', gameState);
    console.log('playerInfo', playerInfo);
  }, [gameState, playerInfo]);
  
  const [isReady, setIsReady] = useState(false); // Î†åÎçîÎßÅ Ï§ÄÎπÑ ÏÉÅÌÉú Ï∂îÍ∞Ä

  const [winner, setWinner] = useState<string | null>(null);
  const [isPaused, setIsPaused] = useState(false);
  const [pauseMessage, setPauseMessage] = useState('');
  const [timeLeft, setTimeLeft] = useState(60);
  const [showTimeoutNotification, setShowTimeoutNotification] = useState(false);
  const [showContinueDialog, setShowContinueDialog] = useState(false);
  const [showQuitDialog, setShowQuitDialog] = useState(false);

  // Redirect to menu if the game page is loaded without necessary state
  useEffect(() => {
    console.log('üîç Game.tsx Ï¥àÍ∏∞Ìôî Ï≤¥ÌÅ¨:', {
      hasInitialState: !!initialState,
      playerId: initialState?.playerId,
      roomId: initialState?.roomId,
      hasGameState: !!initialState?.gameState,
      hasPlayerInfo: !!initialState?.playerInfo,
      urlRoomId: roomId
    });
    
    if (!initialState || !initialState.playerId || !initialState.roomId) {
      console.error("Game.tsx: ÌïÑÏàò Í≤åÏûÑ Îç∞Ïù¥ÌÑ∞Í∞Ä ÏóÜÏäµÎãàÎã§. Î©îÎâ¥Î°ú Ïù¥ÎèôÌï©ÎãàÎã§.");
      navigate('/menu', { replace: true });
      return;
    }
    
    // Í≤åÏûÑ Îç∞Ïù¥ÌÑ∞Í∞Ä ÏûàÏúºÎ©¥ Ï§ÄÎπÑ ÏôÑÎ£å
    setIsReady(true);
    console.log('‚úÖ Game.tsx Ï¥àÍ∏∞Ìôî ÏôÑÎ£å');
  }, [initialState, navigate, roomId]);


  const resetTimer = useCallback(() => {
    setTimeLeft(60);
  }, []);

  useEffect(() => {
    let timer: NodeJS.Timeout;
    
    if (!isPaused && gameState?.currentTurn && !winner) {
      timer = setInterval(() => {
        setTimeLeft(prev => {
          if (prev <= 1) {
            if (socket && gameState.currentTurn === playerId) {
              socket.emit('turnTimeout');
            }
            return 0;
          }
          return prev - 1;
        });
      }, 1000);
    }

    return () => {
      if (timer) clearInterval(timer);
    };
  }, [isPaused, gameState?.currentTurn, winner, socket, playerId]);

  useEffect(() => {
    const token = localStorage.getItem('token');
    if (!token) {
      navigate('/login');
      return;
    }

    // Ï†ÑÏó≠ ÏÜåÏºìÏù¥ ÏûàÎäî Í≤ΩÏö∞ÏóêÎßå Ïù¥Î≤§Ìä∏ Î¶¨Ïä§ÎÑà ÏÑ§Ï†ï
    if (socket) {
      console.log('üéÆ Game.tsxÏóêÏÑú ÏÜåÏºì Ïù¥Î≤§Ìä∏ Î¶¨Ïä§ÎÑà ÏÑ§Ï†ï');

      socket.on('connect_error', (error: Error) => {
        if (error.message === 'Ïù∏Ï¶ùÏù¥ ÌïÑÏöîÌï©ÎãàÎã§.') {
          localStorage.removeItem('token');
          localStorage.removeItem('user');
          navigate('/login');
        }
      });

      // This event might be redundant if state is passed correctly via navigation
      socket.on('playerAssigned', (assignedPlayerId: string | null) => {
        if (!playerId) setPlayerId(assignedPlayerId);
      });

      // This listener is crucial for the *other* player who didn't initiate the navigation
      // But since both players are navigated from MainMenu, this might just be for safety.
      socket.on('gameStarted', (data: GameStartData) => {
        console.log('üéÆ Í≤åÏûÑ ÏãúÏûë Îç∞Ïù¥ÌÑ∞ ÏÉÅÏÑ∏ (from socket event):', data);
        // Only update if the state is not already set or for a different room
        if (!playerId || !playerInfo) {
            setPlayerId(data.playerId);
            setGameState(data.gameState);
            setPlayerInfo(data.playerInfo);
            console.log('ÌîåÎ†àÏù¥Ïñ¥ Ï†ïÎ≥¥ ÏÑ§Ï†ï ÏôÑÎ£å (from socket event):', {
              ÏÑ§Ï†ïÎêúPlayerId: data.playerId,
              ÏÑ§Ï†ïÎêúPlayerInfo: data.playerInfo
            });
            resetTimer();
        }
      });

      socket.on('gameStateUpdate', (newGameState: GameState) => {
        console.log('üîÑ Í≤åÏûÑ ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏:', {
          currentTurn: newGameState.currentTurn,
          myPlayerId: playerId,
          isMyTurn: newGameState.currentTurn === playerId
        });
        setGameState(newGameState);
        resetTimer();
      });

      socket.on('gameState', (newGameState: GameState) => {
        console.log('üîÑ Í≤åÏûÑ ÏÉÅÌÉú (gameState Ïù¥Î≤§Ìä∏):', newGameState);
        setGameState(newGameState);
        resetTimer();
      });
      
      // Ï¥àÍ∏∞ Í≤åÏûÑ ÏÉÅÌÉúÍ∞Ä ÏóÜÎäî Í≤ΩÏö∞ ÏÑúÎ≤ÑÏóê ÏöîÏ≤≠
      if (roomId && (!gameState || !playerId)) {
        console.log('üîÑ Ï¥àÍ∏∞ Í≤åÏûÑ ÏÉÅÌÉú ÏöîÏ≤≠:', roomId);
        socket.emit('requestInitialGameState', { roomId });
      }

      socket.on('turnTimedOut', (message: string) => {
        console.log('üïê ÌÑ¥ ÌÉÄÏûÑÏïÑÏõÉ:', message);
        setShowTimeoutNotification(true);
        setTimeout(() => setShowTimeoutNotification(false), 3000);
        resetTimer();
      });

      socket.on('gameOver', (winnerId: string) => {
        console.log('üèÅ Í≤åÏûÑ Ï¢ÖÎ£å:', winnerId);
        setWinner(winnerId);
      });

      socket.on('gamePaused', (message: string) => {
        setIsPaused(true);
        setPauseMessage(message);
      });

      socket.on('gameResumed', () => {
        setIsPaused(false);
        setPauseMessage('');
        resetTimer();
      });

      socket.on('playerDisconnected', (message: string) => {
        console.log('üö™ ÌîåÎ†àÏù¥Ïñ¥ Ïó∞Í≤∞ Ìï¥Ï†ú:', message);
        setIsPaused(true);
        setPauseMessage(message);
      });

      return () => {
        // Ïù¥Î≤§Ìä∏ Î¶¨Ïä§ÎÑà Ï†ïÎ¶¨
        socket.off('connect_error');
        socket.off('playerAssigned');
        socket.off('gameStarted');
        socket.off('gameState');
        socket.off('turnTimedOut');
        socket.off('gameOver');
        socket.off('gamePaused');
        socket.off('gameResumed');
        socket.off('playerDisconnected');
      };
    }
  }, [socket, navigate, resetTimer, playerId]);

  const handleCellClick = (position: Position) => {
    console.log(`[Game.tsx] handleCellClick received from Board:`, position);
    if (socket && gameState?.currentTurn === playerId && !winner && !isPaused) {
      let serverPosition = position;
      // Player2Ïù∏ Í≤ΩÏö∞, ÏÑúÎ≤ÑÏùò Ï†àÎåÄ Ï¢åÌëúÍ≥Ñ(player1 Í∏∞Ï§Ä)Î°ú Î≥ÄÌôò
      if (playerId === 'player2') {
        serverPosition = {
          x: 8 - position.x,
          y: 8 - position.y,
        };
        console.log(`[Game.tsx] P2 Ï¢åÌëú Î≥ÄÌôò (Move):`, { from: position, to: serverPosition });
      }
      
      console.log(`[Game.tsx] 'move' Ïù¥Î≤§Ìä∏ Ï†ÑÏÜ°:`, serverPosition);
      socket.emit('move', serverPosition);

    } else {
      console.warn(`[Game.tsx] Move ignored. Conditions not met:`, {
        socketExists: !!socket,
        isMyTurn: gameState?.currentTurn === playerId,
        isWinner: !!winner,
        isPaused: isPaused,
      });
    }
  };

  const handleWallPlacement = (wall: Wall) => {
    if (socket && gameState?.currentTurn === playerId && !winner && !isPaused) {
      let serverWall = wall;
      // Player2Ïù∏ Í≤ΩÏö∞, ÏÑúÎ≤ÑÏùò Ï†àÎåÄ Ï¢åÌëúÍ≥Ñ(player1 Í∏∞Ï§Ä)Î°ú Î≥ÄÌôò
      if (playerId === 'player2') {
        serverWall = {
          ...wall,
          position: {
            x: wall.orientation === 'horizontal' ? 7 - wall.position.x : 8 - wall.position.x,
            y: wall.orientation === 'horizontal' ? 8 - wall.position.y : 7 - wall.position.y,
          },
        };
        console.log(`[Game.tsx] P2 Ï¢åÌëú Î≥ÄÌôò (Wall):`, { from: wall, to: serverWall });
      }
      
      console.log(`[Game.tsx] 'placeWall' Ïù¥Î≤§Ìä∏ Ï†ÑÏÜ°:`, serverWall);
      socket.emit('placeWall', serverWall);
    }
  };

  const handleQuit = () => {
    setShowQuitDialog(true);
  };

  const handleQuitConfirm = () => {
    if (socket) {
      socket.emit('forfeit'); // ÏÑúÎ≤ÑÏóê Í∏∞Í∂å Ïã†Ìò∏ Ï†ÑÏÜ°
    }
    navigate('/menu');
  };

  const handleQuitCancel = () => {
    setShowQuitDialog(false);
  };

  const showQuitConfirmDialog = () => {
    setShowQuitDialog(true);
  };

  // Í≤åÏûÑ ÏÉÅÌÉúÎ•º ÌîåÎ†àÏù¥Ïñ¥ Í¥ÄÏ†êÏúºÎ°ú Î≥ÄÌôò (Í∞ÅÏûê ÌïòÎã®ÏóêÏÑú ÏãúÏûëÌïòÎèÑÎ°ù)
  const getGameState = (): (GameState & { players: Player[] }) | null => {
    if (!gameState) return null;

    // ÌÉÄÏûÖ Ïò§Î•ò Ïö∞Ìöå: Ïã§Ï†ú Îç∞Ïù¥ÌÑ∞ Íµ¨Ï°∞Ïóê ÎßûÍ≤å player1, player2Î•º as anyÎ°ú Ï†ëÍ∑º
    const gs: any = gameState;
    const players: Player[] = [
      { id: 'player1', ...gs.player1 },
      { id: 'player2', ...gs.player2 }
    ];
    const safeWalls = gs.walls ?? [];

    if (playerId === 'player2') {
      const transformedPlayers = players.map(player => ({
        ...player,
        position: {
          x: 8 - player.position.x,
          y: 8 - player.position.y
        }
      }));
      const transformedWalls = safeWalls.map((wall: any) => ({
        ...wall,
        position: {
          x: wall.orientation === 'horizontal' ? 7 - wall.position.x : 8 - wall.position.x,
          y: wall.orientation === 'horizontal' ? 8 - wall.position.y : 7 - wall.position.y
        }
      }));
      return { ...gs, players: transformedPlayers, walls: transformedWalls };
    }
    return { ...gs, players, walls: safeWalls };
  };

  const renderPlayerCard = (player: Player, position: 'top' | 'bottom') => {
    if (!gameState) return null;
    // ÏõêÎ≥∏ gameStateÏùò currentTurnÍ≥º ÎπÑÍµêÌï¥Ïïº Ìï® (Î≥ÄÌôòÎêú ÏÉÅÌÉúÍ∞Ä ÏïÑÎãå ÏõêÎ≥∏ ÏÉÅÌÉú ÏÇ¨Ïö©)
    const isCurrentTurn = gameState.currentTurn === player.id;
    const isPlayer1 = player.id === 'player1';
    const isMe = player.id === playerId;
    
    const wallIcons = Array.from({ length: 10 }, (_, i) => (
      <WallIcon key={i} isActive={i < player.wallsLeft} />
    ));

    // ÌîåÎ†àÏù¥Ïñ¥ Ïù¥Î¶Ñ Í≤∞Ï†ï Î°úÏßÅ Í∞úÏÑ†
    let playerName = 'Ïïå Ïàò ÏóÜÏùå';
    
    if (isMe) {
      // ÎÇ¥ Ï†ïÎ≥¥Ïù∏ Í≤ΩÏö∞
      if (playerInfo?.me?.username) {
        playerName = playerInfo.me.username;
      } else {
        // localStorageÏóêÏÑú ÏÇ¨Ïö©Ïûê Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
        try {
          const userStr = localStorage.getItem('user');
          if (userStr) {
            const user = JSON.parse(userStr);
            playerName = user.username || `ÎÇò (${player.id})`;
          } else {
            playerName = `ÎÇò (${player.id})`;
          }
        } catch (error) {
          playerName = `ÎÇò (${player.id})`;
        }
      }
    } else {
      // ÏÉÅÎåÄÎ∞© Ï†ïÎ≥¥Ïù∏ Í≤ΩÏö∞
      if (playerInfo?.opponent?.username) {
        playerName = playerInfo.opponent.username;
      } else {
        playerName = `ÏÉÅÎåÄ (${player.id})`;
      }
    }

    return (
      <PlayerCard 
        key={player.id}
        isCurrentTurn={isCurrentTurn} 
        isPlayer1={isPlayer1}
        position={position}
      >
        <PlayerAvatar isPlayer1={isPlayer1}>
          {isPlayer1 ? 'üî¥' : 'üîµ'}
        </PlayerAvatar>
        <PlayerDetails>
          <PlayerHeader>
            <PlayerName>
              {playerName}
            </PlayerName>
            <PlayerTimer 
              isTimeRunningOut={timeLeft <= 10} 
              isActive={isCurrentTurn}
            >
              {isCurrentTurn && gameState ? `‚è±Ô∏è ${timeLeft}Ï¥à` : 'ÎåÄÍ∏∞ Ï§ë'}
            </PlayerTimer>
          </PlayerHeader>
          <WallInfo>
            <WallIconContainer>
              {wallIcons}
            </WallIconContainer>
            <WallCount>{player.wallsLeft}</WallCount>
          </WallInfo>
        </PlayerDetails>
      </PlayerCard>
    );
  };

  // Î°úÎî© ÏÉÅÌÉú Ï≤òÎ¶¨
  if (!isReady) {
    return (
      <GameOverlay>
        <div className="loading-spinner" style={{marginBottom: '20px'}}></div>
        Í≤åÏûÑÏóê Ï†ëÏÜçÌïòÎäî Ï§ëÏûÖÎãàÎã§...
      </GameOverlay>
    );
  }

  // Í≤åÏûÑ Îç∞Ïù¥ÌÑ∞Í∞Ä Ï§ÄÎπÑÎêòÏßÄ ÏïäÏïòÏúºÎ©¥ Î°úÎî© ÌôîÎ©¥ ÌëúÏãú
  if (!isReady || !gameState || !playerId) {
    return (
      <GameContainer>
        <GameOverlay>
          <div style={{ textAlign: 'center' }}>
            <h2>üéÆ Í≤åÏûÑ Î°úÎî© Ï§ë...</h2>
            <p>Í≤åÏûÑ Îç∞Ïù¥ÌÑ∞Î•º Î∂àÎü¨Ïò§Í≥† ÏûàÏäµÎãàÎã§.</p>
            <div className="loading-spinner" style={{ margin: '20px auto' }}></div>
          </div>
        </GameOverlay>
      </GameContainer>
    );
  }

  // ÌîåÎ†àÏù¥Ïñ¥ Ï†ïÎ≥¥Îäî ÏõêÎ≥∏ Í≤åÏûÑ ÏÉÅÌÉúÏóêÏÑú Í∞ÄÏ†∏Ïò§Í≥†, ÌôîÎ©¥ ÌëúÏãúÏö© ÏÉÅÌÉúÎäî Îî∞Î°ú Î≥ÄÌôò
  const transformedGameState = getGameState();
  if (!transformedGameState) {
      console.error("Render crash: transformedGameState is null even when ready.");
      return (
        <GameContainer>
          <GameOverlay>
              Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§. Î©îÎâ¥Î°ú ÎèåÏïÑÍ∞ëÎãàÎã§...
          </GameOverlay>
        </GameContainer>
      );
  }

  const myPlayer = transformedGameState.players.find((p: Player) => p.id === playerId);
  const opponentPlayer = transformedGameState.players.find((p: Player) => p.id !== playerId);

  return (
    <GameContainer>
      <Header>
        <Title>Quoridor</Title>
        <HeaderQuitButton onClick={showQuitConfirmDialog}>
          Í∏∞Í∂åÌïòÍ∏∞
        </HeaderQuitButton>
      </Header>
      
      <GameArea>
        {opponentPlayer && (
          <InfoContainer>
            {renderPlayerCard(opponentPlayer, 'top')}
          </InfoContainer>
        )}
        <BoardArea>
          <Board 
            gameState={transformedGameState} 
            onCellClick={handleCellClick}
            onWallPlace={handleWallPlacement}
            playerId={playerId}
            isMyTurn={gameState!.currentTurn === playerId}
          />
        </BoardArea>
        {myPlayer && (
          <InfoContainer>
            {renderPlayerCard(myPlayer, 'bottom')}
          </InfoContainer>
        )}
      </GameArea>

      {winner && (
        <Dialog>
          <DialogTitle>Í≤åÏûÑ Ï¢ÖÎ£å</DialogTitle>
          <DialogMessage>
            {winner === playerId ? 'Ï∂ïÌïòÌï©ÎãàÎã§! ÎãπÏã†Ïù¥ Ïù¥Í≤ºÏäµÎãàÎã§!' : 'ÏïÑÏâΩÍ≤åÎèÑ ÎãπÏã†Ïù¥ Ï°åÏäµÎãàÎã§.'}
          </DialogMessage>
          <DialogButtons>
            <DialogButton variant="confirm" onClick={() => navigate('/menu')}>
              ÌôïÏù∏
            </DialogButton>
          </DialogButtons>
        </Dialog>
      )}

      {showQuitDialog && (
        <Dialog>
          <DialogTitle>Í≤åÏûÑÏùÑ ÎÇòÍ∞ÄÏãúÍ≤†ÏäµÎãàÍπå?</DialogTitle>
          <DialogMessage>
            Í≤åÏûÑÏùÑ ÎÇòÍ∞ÄÎ©¥ Ìå®Î∞∞Î°ú Ï≤òÎ¶¨Îê©ÎãàÎã§.<br />
            Ï†ïÎßêÎ°ú ÎÇòÍ∞ÄÏãúÍ≤†ÏäµÎãàÍπå?
          </DialogMessage>
          <DialogButtons>
            <DialogButton variant="cancel" onClick={handleQuitCancel}>
              Ï∑®ÏÜå
            </DialogButton>
            <DialogButton variant="confirm" onClick={handleQuitConfirm}>
              ÎÇòÍ∞ÄÍ∏∞
            </DialogButton>
          </DialogButtons>
        </Dialog>
      )}

      {isPaused && (
        <GameOverlay>
          ‚è∏Ô∏è Í≤åÏûÑÏù¥ ÏùºÏãúÏ†ïÏßÄÎêòÏóàÏäµÎãàÎã§
          <br />
          <div style={{ fontSize: '18px', marginTop: '10px' }}>
            {pauseMessage}
          </div>
        </GameOverlay>
      )}
    </GameContainer>
  );
}

export default Game;